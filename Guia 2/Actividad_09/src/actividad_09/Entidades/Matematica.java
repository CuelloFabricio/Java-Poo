package actividad_09.Entidades;


public class Matematica {
    private double Num1;
    private double Num2;

    public Matematica() {
    }

    public Matematica(double Num1, double Num2) {
        this.Num1 = Num1;
        this.Num2 = Num2;
    }

    public double getNum1() {
        return Num1;
    }

    public void setNum1(double Num1) {
        this.Num1 = Num1;
    }

    public double getNum2() {
        return Num2;
    }

    public void setNum2(double Num2) {
        this.Num2 = Num2;
    }

}

